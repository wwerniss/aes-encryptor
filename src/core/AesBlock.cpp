    const uint8_t rcon[11] = {
        0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36
    };

    uint8_t mul2(uint8_t a) {
        return (a << 1) ^ ((a & 0x80) ? 0x1b : 0x00);
    }
