std::array<uint8_t, AesBlock::BLOCK_SIZE> AesBlock::encryptBlock(
    const std::array<uint8_t, BLOCK_SIZE>& input,
    const std::array<uint8_t, KEY_SIZE>& key) 
{
    std::array<uint8_t, BLOCK_SIZE> state = input;
    std::vector<uint8_t> expandedKey = expandKey(key);

    addRoundKey(state, expandedKey, 0);

    for (size_t round = 1; round < NUM_ROUNDS; ++round) {
        subBytes(state);
        shiftRows(state);
        mixColumns(state);
        addRoundKey(state, expandedKey, round);
    }

    subBytes(state);
    shiftRows(state);
    addRoundKey(state, expandedKey, NUM_ROUNDS);

    return state;
}

void AesBlock::addRoundKey(std::array<uint8_t, BLOCK_SIZE>& state, const std::vector<uint8_t>& roundKey, size_t round) {
    size_t keyOffset = round * BLOCK_SIZE;
    for (size_t i = 0; i < BLOCK_SIZE; ++i) {
        state[i] ^= roundKey[keyOffset + i];
    }
}

void AesBlock::subBytes(std::array<uint8_t, BLOCK_SIZE>& state) {
    for (size_t i = 0; i < BLOCK_SIZE; ++i) {
        state[i] = sbox[state[i]];
    }
}

void AesBlock::shiftRows(std::array<uint8_t, BLOCK_SIZE>& state) {
    std::array<uint8_t, BLOCK_SIZE> temp = state;

    // Row 0 doesn't shift
    // Row 1 shifts left by 1
    state[1] = temp[5];
    state[5] = temp[9];
    state[9] = temp[13];
    state[13] = temp[1];

    // Row 2 shifts left by 2
    state[2] = temp[10];
    state[6] = temp[14];
    state[10] = temp[2];
    state[14] = temp[6];

    // Row 3 shifts left by 3
    state[3] = temp[15];
    state[7] = temp[3];
    state[11] = temp[7];
    state[15] = temp[11];
}

void AesBlock::mixColumns(std::array<uint8_t, BLOCK_SIZE>& state) {
    std::array<uint8_t, BLOCK_SIZE> temp = state;
    for (size_t i = 0; i < 4; ++i) {
        size_t c = i * 4;
        uint8_t a0 = temp[c];
        uint8_t a1 = temp[c + 1];
        uint8_t a2 = temp[c + 2];
        uint8_t a3 = temp[c + 3];

        state[c]     = mul2(a0) ^ mul2(a1) ^ a1 ^ a2 ^ a3;
        state[c + 1] = a0 ^ mul2(a1) ^ mul2(a2) ^ a2 ^ a3;
        state[c + 2] = a0 ^ a1 ^ mul2(a2) ^ mul2(a3) ^ a3;
        state[c + 3] = mul2(a0) ^ a0 ^ a1 ^ a2 ^ mul2(a3);
    }
}

std::vector<uint8_t> AesBlock::expandKey(const std::array<uint8_t, KEY_SIZE>& key) {
    std::vector<uint8_t> expanded(BLOCK_SIZE * (NUM_ROUNDS + 1));
    for (size_t i = 0; i < KEY_SIZE; ++i) {
        expanded[i] = key[i];
    }

    for (size_t i = KEY_SIZE; i < expanded.size(); i += 4) {
        std::array<uint8_t, 4> temp = {expanded[i - 4], expanded[i - 3], expanded[i - 2], expanded[i - 1]};

        if (i % KEY_SIZE == 0) {
            // RotWord
            uint8_t t = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = t;

            // SubWord
            temp[0] = sbox[temp[0]];
            temp[1] = sbox[temp[1]];
            temp[2] = sbox[temp[2]];
            temp[3] = sbox[temp[3]];

            // Rcon
            temp[0] ^= rcon[i / KEY_SIZE];
        }

        expanded[i]     = expanded[i - KEY_SIZE] ^ temp[0];
        expanded[i + 1] = expanded[i - KEY_SIZE + 1] ^ temp[1];
        expanded[i + 2] = expanded[i - KEY_SIZE + 2] ^ temp[2];
        expanded[i + 3] = expanded[i - KEY_SIZE + 3] ^ temp[3];
    }

    return expanded;
}
